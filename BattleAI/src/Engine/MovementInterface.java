package Engine;

public interface MovementInterface {

    /**
     * Moves the entity up.
     */
    public void moveUp();

    /**
     * Moves the entity down.
     */
    public void moveDown();

    /**
     * Moves the entity left
     */
    public void moveLeft();

    /**
     * Moves the entity right.
     */
    public void moveRight();
}
